const categories = [
  "all",
  "foliage",
  "extra",
  "fences",
  "buildings",
  "path decor",
  "foods",
  "stands",
  "plushies",
  "pillars",
  "arches",
  "lamp posts",
  "bridges",
  "stencils"
];

let activeCategory = "all";
const selected = new Set();

const owned = new Set(
  JSON.parse(localStorage.getItem("ownedAssets") || "[]").map(Number)
);

const nav = document.getElementById("category-nav");
const grid = document.getElementById("asset-grid");

function saveOwnedAssets() {
  localStorage.setItem("ownedAssets", JSON.stringify([...owned]));
}

function renderCategories() {
  nav.innerHTML = categories
    .map(
      (category) =>
        `<button class="category-button ${
          category === activeCategory ? "active" : ""
        }" type="button" data-category="${category}">${category}</button>`
    )
    .join("");
}

function renderCards() {
  const visibleAssets = assetCatalog.filter(
    (asset) => activeCategory === "all" || asset.category === activeCategory
  );

  document.getElementById(
    "item-count"
  ).textContent = `${visibleAssets.length} assets`;

  grid.innerHTML = visibleAssets
    .map(
      (asset) => `
      <button
        class="asset-card ${selected.has(asset.id) ? "selected" : ""} ${
          owned.has(asset.id) ? "owned" : ""
        }"
        type="button"
        data-id="${asset.id}"
        aria-label="Select ${asset.name}"
      >
        <span
          class="owned-button"
          data-owned-id="${asset.id}"
          role="button"
          tabindex="0"
          aria-label="${
            owned.has(asset.id)
              ? `Remove ${asset.name} from owned items`
              : `Mark ${asset.name} as owned`
          }"
        >
          ${owned.has(asset.id) ? "✓" : "♡"}
        </span>

        <img class="asset-image" src="${asset.image}" alt="${asset.name}">

        <span class="asset-copy">
          <span class="robux">R$ ${asset.robux}</span>
          <span class="euro">€${asset.euro.toFixed(2)}</span>
        </span>
      </button>
    `
    )
    .join("");
}

function updateTotal() {
  const pickedAssets = assetCatalog.filter((asset) => selected.has(asset.id));

  const robuxTotal = pickedAssets.reduce(
    (sum, asset) => sum + asset.robux,
    0
  );

  const euroTotal = pickedAssets.reduce(
    (sum, asset) => sum + asset.euro,
    0
  );

  document.getElementById(
    "total-robux"
  ).textContent = `R$ ${robuxTotal.toLocaleString()}`;

  document.getElementById(
    "total-euros"
  ).textContent = `€${euroTotal.toFixed(2)}`;

  document.getElementById(
    "selection-count"
  ).textContent = `${pickedAssets.length} selected`;
}

nav.addEventListener("click", (event) => {
  const button = event.target.closest("[data-category]");
  if (!button) return;

  activeCategory = button.dataset.category;
  renderCategories();
  renderCards();
});

grid.addEventListener("click", (event) => {
  const ownedButton = event.target.closest("[data-owned-id]");

  if (ownedButton) {
    event.preventDefault();
    event.stopPropagation();

    const id = Number(ownedButton.dataset.ownedId);

    if (owned.has(id)) {
      owned.delete(id);
    } else {
      owned.add(id);
    }

    saveOwnedAssets();
    renderCards();
    return;
  }

  const card = event.target.closest("[data-id]");
  if (!card) return;

  const id = Number(card.dataset.id);

  if (selected.has(id)) {
    selected.delete(id);
  } else {
    selected.add(id);
  }

  renderCards();
  updateTotal();
});

grid.addEventListener("keydown", (event) => {
  const ownedButton = event.target.closest("[data-owned-id]");

  if (!ownedButton || (event.key !== "Enter" && event.key !== " ")) return;

  event.preventDefault();
  ownedButton.click();
});

renderCategories();
renderCards();
updateTotal();
